namespace SoftwareEngineerSkills.Domain.Enums;

/// <summary>
/// Represents the difficulty level of a skill
/// </summary>
public enum SkillLevel
{
    /// <summary>
    /// Beginner level - basic understanding and minimal experience
    /// </summary>
    Beginner = 1,
    
    /// <summary>
    /// Elementary level - can perform basic tasks with guidance
    /// </summary>
    Elementary = 2,
    
    /// <summary>
    /// Intermediate level - can work independently on routine tasks
    /// </summary>
    Intermediate = 3,
    
    /// <summary>
    /// Advanced level - deep understanding and can tackle complex problems
    /// </summary>
    Advanced = 4,
    
    /// <summary>
    /// Expert level - comprehensive knowledge and can innovate and teach
    /// </summary>
    Expert = 5
}
