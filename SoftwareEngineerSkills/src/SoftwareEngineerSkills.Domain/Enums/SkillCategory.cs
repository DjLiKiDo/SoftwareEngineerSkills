namespace SoftwareEngineerSkills.Domain.Enums;

/// <summary>
/// Represents common categories of software engineering skills
/// </summary>
public enum SkillCategory
{
    /// <summary>
    /// Programming languages (e.g., C#, Java, JavaScript)
    /// </summary>
    ProgrammingLanguage,
    
    /// <summary>
    /// Programming skills and practices
    /// </summary>
    Programming,
    
    /// <summary>
    /// Web development skills and technologies
    /// </summary>
    WebDevelopment,
    
    /// <summary>
    /// Frameworks and libraries (e.g., ASP.NET Core, React, Angular)
    /// </summary>
    Framework,
    
    /// <summary>
    /// Database technologies (e.g., SQL Server, MongoDB, Redis)
    /// </summary>
    Database,
    
    /// <summary>
    /// Cloud platforms and services (e.g., Azure, AWS, GCP)
    /// </summary>
    Cloud,
    
    /// <summary>
    /// DevOps tools and practices (e.g., Docker, Kubernetes, CI/CD)
    /// </summary>
    DevOps,
    
    /// <summary>
    /// Testing frameworks and practices (e.g., xUnit, Jest, TDD)
    /// </summary>
    Testing,
    
    /// <summary>
    /// Architecture patterns and practices (e.g., Microservices, DDD)
    /// </summary>
    Architecture,
    
    /// <summary>
    /// Development tools and utilities (e.g., Git, VS Code, Visual Studio)
    /// </summary>
    Tool,
    
    /// <summary>
    /// Soft skills for software engineers (e.g., Communication, Problem-solving)
    /// </summary>
    SoftSkill,
    
    /// <summary>
    /// Other skills that don't fit into the above categories
    /// </summary>
    Other
}
